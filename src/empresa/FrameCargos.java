    private void llenarTabla() {
        ArrayList<Cargos> ls = carBll.leer();
        
        DefaultTableModel modelo = (DefaultTableModel) tbCargos.getModel();
        modelo.setRowCount(0);
        
        for (int i = 0; i < ls.size(); i++) {
            Object[] vec = new Object[2];
            vec[0] = ls.get(i).getId();
            vec[1] = ls.get(i).getNombre();
            
            modelo.addRow(vec);
        }
        
    }
    
    private void guardar() {
        
        Cargos cargo = new Cargos();
        cargo.setId(Integer.parseInt(txtId.getText()));
        cargo.setNombre(txtNombre.getText());
        
        if (carBll.buscarId(cargo.getId()) == null) {
            
            carBll.guardar(cargo);
            JOptionPane.showMessageDialog(null, "REGISTRADO CORRECTAMENTE");
            
        } else {
            
            carBll.actualizar(cargo);
            JOptionPane.showMessageDialog(null, "ACTUALIZADO CORRECTAMENTE");
            
        }
        llenarTabla();
        
    }
    
    private void eliminar(int id) {
        carBll.eliminar(id);
        
        JOptionPane.showMessageDialog(null, "ELIMINADO CORRECTAMENTE");
        llenarTabla();
    }
    
